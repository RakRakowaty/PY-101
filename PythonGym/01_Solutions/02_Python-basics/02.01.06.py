# Declare two string variables, firstName and lastName, with your first and last names
firstName = "Your"
lastName = "Name"

# Concatenate firstName and lastName with a space to form the full name
fullName = firstName + " " + lastName
# Alternative solution:
fullName = f"{firstName} {lastName}"

# Print a message using string formatting to insert the full name
print(f"Hello, my name is {fullName}.")
